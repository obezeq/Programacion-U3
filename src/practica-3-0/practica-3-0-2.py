#!/usr/bin/env python3

fruta = "fruta"

print(fruta[:])

# Al hacer fruta[:], es como si pusieramos fruta[0:len(fruta)], pues de esta forma estamos recorriendo todos los caracteres del texto.
# Otro ejemplo de la utilización de ":" en el filtrado de listas [], es si introducimos fruta[1:], estariamos imprimiendo desde el cacractér 1, hasta el final.